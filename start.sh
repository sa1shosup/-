#!/bin/bash

# Скрипт для запуска бота в фоновом режиме

# Создание директорий, если они не существуют
mkdir -p assets fonts temp logs

# Запуск бота с перенаправлением вывода в лог
echo "Запуск телеграм-бота для создания документов..."
python run.py > logs/bot.log 2>&1 &

# Сохранение PID бота
echo $! > bot.pid
echo "Бот запущен с PID: $(cat bot.pid)"
echo "Логи сохраняются в файл logs/bot.log"
echo "Для остановки бота используйте: ./stop.sh"